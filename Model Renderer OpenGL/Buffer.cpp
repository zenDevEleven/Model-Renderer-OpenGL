void Buffer::Init()
{
	std::cout << "Created vao and vbo buffers" << std::endl;
	glGenBuffers(1, &vbo);
	glBindBuffer(GL_ARRAY_BUFFER, vbo);

	glGenVertexArrays(1, &vao);
	glBindVertexArray(vao);
}


